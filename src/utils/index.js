export function debounce(func, wait, immediate) {
  let timeout, args, context, timestamp, result;

  const later = function() {
    // 据上一次触发时间间隔
    const last = +new Date() - timestamp;

    // 上次被包装函数被调用时间间隔last小于设定时间间隔wait
    if (last < wait && last > 0) {
      timeout = setTimeout(later, wait - last);
    } else {
      timeout = null;
      // 如果设定为immediate===true，因为开始边界已经调用过了此处无需调用
      if (!immediate) {
        result = func.apply(context, args);
        if (!timeout) context = args = null;
      }
    }
  };

  return function(...args) {
    context = this;
    timestamp = +new Date();
    const callNow = immediate && !timeout;
    // 如果延时不存在，重新设定延时
    if (!timeout) timeout = setTimeout(later, wait);
    if (callNow) {
      result = func.apply(context, args);
      context = args = null;
    }

    return result;
  };
}

/**
 * This is just a simple version of deep copy
 * Has a lot of edge cases bug
 * If you want to use a perfect deep copy, use lodash's _.cloneDeep
 */
export function deepClone(source) {
  if (!source && typeof source !== 'object') {
    throw new Error('error arguments', 'shallowClone');
  }
  const targetObj = source.constructor === Array ? [] : {};
  Object.keys(source).forEach((keys) => {
    if (source[keys] && typeof source[keys] === 'object') {
      targetObj[keys] = deepClone(source[keys]);
    } else {
      targetObj[keys] = source[keys];
    }
  });
  return targetObj;
}

export function uniqueArr(arr) {
  return Array.from(new Set(arr));
}

export function createUniqueString() {
  const timestamp = +new Date() + '';
  const randomNum = parseInt((1 + Math.random()) * 65536) + '';
  return (+(randomNum + timestamp)).toString(32);
}

/**
 * @param {Array} arr1
 * @param {Array} arr2
 * @returns {Array}
 */
export function diffArary(arr1, arr2) {
  arr1 = uniqueArr(arr1);
  arr2 = uniqueArr(arr2);
  return arr1.concat(arr2).filter(arg => !(arr1.includes(arg) && arr2.includes(arg)));
}

/**
 * compressImage  图片压缩
 * url 图片地址，base64, url
 * type 图片输出格式
 * quality 图片压缩程度 0-1
 * callback 回调返回压缩后的图片 base64格式
  */
export function compressImage(url, type = 'image/png', quality = 1, callback) {
  var ready = new FileReader();
  ready.readAsDataURL(url);
  ready.onload = (e) => {
    var Img = new Image();
    var dataURL = '';
    Img.src = e.target.result;
    Img.onload = () => {
      // 要先确保图片完整获取到，这是个异步事件
      var canvas = document.createElement('canvas'); // 创建canvas元素
      var width = Img.width; // 确保canvas的尺寸和图片一样
      var height = Img.height;
      canvas.width = width;
      canvas.height = height;
      canvas.getContext('2d').drawImage(Img, 0, 0, width, height); // 将图片绘制到canvas中
      dataURL = canvas.toDataURL(type, quality); // 转换图片为dataURL 图片质量0~1默认值0.92
      callback ? callback(dataURL) : null; // 调用回调函数
    };
  };
}

/**
 * base64Size  计算base64格式文件大小
 * base64
 */

export function base64Size(base64) {
  var str = base64.split(',')[1];
  var equalIndex = str.indexOf('=');
  if (str.indexOf('=') > 0) {
    str = str.substring(0, equalIndex);
  }
  var strLength = str.length;
  var fileLength = parseInt(strLength - (strLength / 8) * 2);
  return fileLength || 0;
}
/**
 * blob -> Base64
 * */
export function convertBase64UrlToBlob(dataurl, filename) {
  var arr = dataurl.split(',');
  var mime = arr[0].match(/:(.*?);/)[1];
  var bstr = atob(arr[1]);
  var n = bstr.length;
  var u8arr = new Uint8Array(n);
  while (n--) {
    u8arr[n] = bstr.charCodeAt(n);
  }
  return new File([u8arr], filename, { type: mime });
}

